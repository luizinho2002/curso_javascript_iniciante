let num; // Criamos uma variável chamada 'num' para armazenar um número

// Inicializamos a variável 'num' com o valor 1. Essa variável será usada como contador.
for(num = 1; num < 6; num++) {
    // Esse loop irá se repetir enquanto 'num' será impresso no console.
    console.log(num);
    // Após a imporessão, o valor de 'num' é incrementado em 1.
}